x1 <- c(1,2,3,1,2,3,1,2,1,2,3,4,3,4)
x2 <- c(4,4,4,3,3,3,2,2,1,1,2,2,1,1)
y <- c(rep(1,6),rep(2,4),rep(3,4))
color <- c("red","green","blue")
plot(x1,x2,col=color[y],main="Decision Tree")
abline(h=2.5)
z <- cbind(2.5,seq(0,2.5,0.0001))
lines(z)

y <- as.factor(y)
data <- data.frame(x1,x2,y)
library("tree")
mod.tree <- tree(y~.,data=data,control=tree.control(nobs=14,minsize=2))
plot(mod.tree,main="Tree Diagram")
text(mod.tree,pretty=0)